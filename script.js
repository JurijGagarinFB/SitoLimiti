// Funzione per calcolare un limite fittizio (in questo caso, un semplice esempio)
document.getElementById("calculateBtn").addEventListener("click", function() {
    const resultDiv = document.getElementById("result");
    
    // Esegui un "calcolo" fittizio del limite (questo può essere un vero calcolo in futuro)
    const limit = 5;  // Supponiamo che il limite di una funzione sia 5
    resultDiv.innerHTML = `Il limite calcolato è: <strong>${limit}</strong>`;
});
